package com.varun.VaSaNi.sample.popular;

public class ProductsClass {
    String description;
    String names;
    String id;
    int quantity;
    int pastorders;
    double price;

    public ProductsClass() {
    }

    public String getDescription() {
        return description;
    }

    public String getNames() {
        return names;
    }

    public String getId() {
        return id;
    }

    public int getQuantity() {
        return quantity;
    }

    public int getPastorders() {
        return pastorders;
    }

    public double getPrice() {
        return price;
    }
}
